/*
 *** iki veya daha fazla verinin karşılaştırılması gerekebilir.
 ==  (eşit ise)
 === (veri türü dahil eşit ise)
 != (eşit değilse)
 !== (veri türü dahil eşit değilse)
 > (büyüktür)
 < (küçüktür)
 >= (büyük eşittir)
 <= (küçük eşittir)
 */

 var sayi = 2
 // == degere bakar
 console.log(sayi == 2)
 console.log(sayi == "2")

 // === türüne de bakar.
 console.log(sayi === 2)
 console.log(sayi === "2")

/*
 * &&   (ve)
 * ||   (veya)
 */

console.log(1 > 4 && 2 == 2)
console.log(1 > 4 || 2 == 2)
