var deger1 = 5,
    deger2 = 5,
    deger3 = 5,
    deger4 = 5;
console.log(deger1++, " deger++ dan sonra: ", deger1)
console.log(++deger2, " ++deger dan sonra: ", deger2)
console.log(deger3--, " deger-- dan sonra: ", deger3)
console.log(--deger4, " --deger dan sonra: ", deger4)

console.log("--------------------------------------------")

/*
 * deger++ -->  deger yazar sonra artar
 * ++deger -->  deger artar sonra yazar
 * deger-- -->  deger yazar sonra azalır
 * --deger -->  deger azalır sonra yazar
 */

